package com.libyasolutions.libyamarketplace.object;

public class MessageObj {
	private String mId, mQuestion, mContent, mNameSub, mEmailSub;
	private int mPhoneSub;

	public String getmId() {
		return mId;
	}

	public void setmId(String mId) {
		this.mId = mId;
	}

	public String getmQuestion() {
		return mQuestion;
	}

	public void setmQuestion(String mQuestion) {
		this.mQuestion = mQuestion;
	}

	public String getmContent() {
		return mContent;
	}

	public void setmContent(String mContent) {
		this.mContent = mContent;
	}

	public String getmNameSub() {
		return mNameSub;
	}

	public void setmNameSub(String mNameSub) {
		this.mNameSub = mNameSub;
	}

	public String getmEmailSub() {
		return mEmailSub;
	}

	public void setmEmailSub(String mEmailSub) {
		this.mEmailSub = mEmailSub;
	}

	public int getmPhoneSub() {
		return mPhoneSub;
	}

	public void setmPhoneSub(int mPhoneSub) {
		this.mPhoneSub = mPhoneSub;
	}

}
